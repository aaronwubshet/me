<h2>Product Management & Strategy </h2>
<p>
  Between my 2nd and 3rd year at Bain, I took a 5 month secondament/externship to work in a product management / strategy role 
  at a satellite launch company called Astra. While my initial role there was focused on building out a strategy for future
  constellations in the form of satellites as a service, I quickly pivoted to focusing on potential physical space products. I was 
  there for 5 months and got to touch a little bit of everything from optimizing launch costs to defining customer needs in the 
