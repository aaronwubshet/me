  constellation market map tool to support evolving market size questions. All of these activities required putting in place work
  flows that would outlast me and required input from multiple stakeholders including Product, Finance, and Sales. Ultimately, I was able 
  to share a significant portion of the existing space products pricing work as well as new space product business plan development 
  work with both the CTO and CEO at multiple points throughout my 5 months. 
